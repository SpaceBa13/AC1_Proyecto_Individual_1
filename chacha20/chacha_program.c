// Simple implementation of basic functions since we're in bare-metal environment
void print_char(char c) {
    // In a real bare-metal environment, this would write to UART
    // For now, this is just a placeholder
    volatile char *uart = (volatile char*)0x10000000;
    *uart = c;
}

// Prints a null-terminated string character by character
// using the low-level print_char function.
void print_string(const char* str) {
    while (*str) {
        print_char(*str++);
    }
}

// Prints a single byte in hexadecimal format (two hex digits).
// Used for displaying ciphertext and keystream values.
void print_hex_byte(unsigned char b) {
    char hex[] = "0123456789abcdef";

    // Print high nibble
    print_char(hex[(b >> 4) & 0xF]);

    // Print low nibble
    print_char(hex[b & 0xF]);
}

// Prints a 32-bit unsigned integer in hexadecimal format (8 hex digits).
// Uses print_char to output each hexadecimal digit.
void print_hex_uint32(uint32_t x) {
    char hex[] = "0123456789abcdef";
    for (int i = 7; i >= 0; i--) {
        print_char(hex[(x >> (i*4)) & 0xF]);
    }
}

