// Prints a block of bytes in hexadecimal format with ASCII representation.
// Bytes are printed in rows of 16, similar to a hex dump. Each byte is also
// stored in the global encrypted_message buffer.
void print_block(unsigned char *block, unsigned int length) {

    // Buffer to store ASCII representation of the current 16-byte line
    static char ascii[16];

    // Iterate through each byte of the block
    for (unsigned int i = 0; i < length; i++) {

        // Start a new line every 16 bytes
        if (encrypted_index % 16 == 0) {

            // If this is not the first line, print the ASCII column
            if (encrypted_index != 0) {
                print_string(" ");

                // Print ASCII characters for the previous 16 bytes
                for (int j = 0; j < 16; j++) {
                    print_char(ascii[j]);
                }

                // Move to next line
                print_string("\n");
            }
        }

        // Get current byte
        unsigned char b = block[i];

        // Store byte in the encrypted message buffer
        encrypted_message[encrypted_index] = b;

        // Print byte in hexadecimal format
        print_hex_byte(b);
        print_char(' ');

        // Save printable ASCII characters, otherwise replace with '.'
        if (b >= 32 && b <= 126)
            ascii[encrypted_index % 16] = b;
        else
            ascii[encrypted_index % 16] = '.';

        // Increment global byte index
        encrypted_index++;
    }

    int remaining = encrypted_index % 16;

    // Only print ASCII if the line is incomplete
    if (remaining != 0) {

        for (int i = remaining; i < 16; i++) {
            print_string("   ");
        }

        print_string(" ");

        for (int i = 0; i < remaining; i++) {
            print_char(ascii[i]);
        }

        print_string("\n");
    }
}

// Saves a serialized 64-byte ChaCha20 block into the global keystream buffer.
// The block is stored starting at the current encrypted_index position.
void save_serialized(unsigned char *block)
{
    for (int i = 0; i < 64; i++)
    {
        keystream[encrypted_index + i] = block[i];
    }
}


// Saves the current ChaCha20 state (16 uint32 words) into the blocks buffer.
// The position is determined by the current block index derived from encrypted_index.
void save_state(uint32_t *state)
{
    unsigned int block = encrypted_index / 64;

    for (int i = 0; i < 16; i++)
    {
        blocks[block * 16 + i] = state[i];
    }
}


// Prints the internal ChaCha20 state blocks used during encryption.
// Each block consists of 16 uint32 values (512 bits) displayed in hexadecimal format.
void print_blocks(int message_len, uint32_t *blocks)
{
    unsigned int num_blocks = (message_len + 63) / 64;

    for (unsigned int b = 0; b < num_blocks; b++)
    {
        for (int i = 0; i < 16; i++)
        {
            print_hex_uint32(blocks[b * 16 + i]);

            if ((i % 4) != 3)
                print_string("  ");
            else
                print_string("\n");
        }

        print_string("\n");
    }
}


// Prints the generated keystream in hexadecimal byte format.
// Bytes are separated by ':' and grouped in lines of 16 bytes.
void print_keystream(int message_len, unsigned char *keystream)
{
    for (unsigned int i = 0; i < message_len; i++)
    {
        print_hex_byte(keystream[i]);

        if (i != message_len - 1)
            print_char(':');

        if ((i + 1) % 16 == 0)
            print_string("\n");
    }
}

